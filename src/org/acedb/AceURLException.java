package org.acedb;

public class AceURLException extends AceException {
  public AceURLException() {
    super();
  }
  
  public AceURLException(String message) {
    super(message);
  }
  
  public AceURLException(Throwable cause) {
    super(cause);
  }
  
  public AceURLException(Throwable cause, String message) {
    super(cause, message);
  }
}
